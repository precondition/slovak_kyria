static inline void tap_accented_letter(uint16_t letter, uint16_t dead_key) {
    uint8_t mod_state = get_mods();
    uint8_t oneshot_mod_state = get_oneshot_mods();
    del_mods(MOD_MASK_SHIFT);
    del_oneshot_mods(MOD_MASK_SHIFT);
    tap_code16(dead_key);
    set_mods(mod_state);
    set_oneshot_mods(oneshot_mod_state);
    tap_code(letter);
}

bool process_record_user(uint16_t keycode, keyrecord_t* record) {
    switch (keycode) {
        case E_ACUTE:
        case R_ACUTE:
        case U_ACUTE:
        case I_ACUTE:
        case O_ACUTE:
        case A_ACUTE:
        case L_ACUTE:
            if (record->event.pressed) {
               tap_accented_letter(keycode, SK_ACUT); 
            }
            return false;

        case T_CARON:
        case Z_CARON:
        case O_CARON:
        case S_CARON:
        case D_CARON:
        case L_CARON:
        case C_CARON:
        case N_CARON:
            if (record->event.pressed) {
                tap_accented_letter(keycode, SK_CARN);
            }
            return false;
    }
    return true;
}

